'''对象的布尔值
	· Python一切皆对象,所有对象都有一个布尔值
	·获取对象的布尔值---使用内置函数bool
·以下对象的布尔值为 False'''
print(bool(False)) #False
print(bool(0)) #数值0
print(bool(None))  #None
print(bool(''))  #空字符串
print(bool([]))  #空列表
print(bool(list()))  #空列表
print(bool(()))  #空元组
print(bool(tuple()))  #空元组
print(bool({}))  #空字典
print(bool(dict()))  #空字典
print(bool(set()))  #空集合
#  其他都是True
print(bool(123))







